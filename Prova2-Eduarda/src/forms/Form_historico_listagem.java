    private void jB_Editar_PesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_Editar_PesoActionPerformed
        int opcao = jT_Pesos.getSelectedRow();
        if (opcao >= 0) {

            Historico h = new Historico();
            h.setIdhist(Integer.parseInt(jT_Pesos.getValueAt(opcao, 6).toString()));
            h.setPeso(Float.parseFloat(jT_Pesos.getValueAt(opcao, 5).toString()));
            h.setDataHist(jT_Pesos.getValueAt(opcao, 2).toString());

            Aluno a = new Aluno();
            a.setNome(jT_Pesos.getValueAt(opcao, 1).toString());
            a.setAltura(Float.parseFloat(jT_Pesos.getValueAt(opcao, 3).toString()));
            a.setIdal(Integer.parseInt(jT_Pesos.getValueAt(opcao, 0).toString()));
            a.setPeso(Float.parseFloat(jT_Pesos.getValueAt(opcao, 4).toString()));
            h.setAluno(a);

            Form_historico fh = new Form_historico(a, h);
            fh.setDadosCallback(this);
            fh.setVisible(true);
            fh.ocultarBotaoSelecionarAluno();
            fh.ocultarBotaoCadastrar();

        }
    }//GEN-LAST:event_jB_Editar_PesoActionPerformed

    private void jB_Excluir_PesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_Excluir_PesoActionPerformed
        int opcao = jT_Pesos.getSelectedRow();
        if (opcao >= 0) {
            Historico h = new Historico();
            h.setIdhist(Integer.parseInt(jT_Pesos.getValueAt(opcao, 6).toString()));

            HistoricoDAO histdao = new HistoricoDAO();
            histdao.excluir(h);
            preencherTabela();
        }

    }//GEN-LAST:event_jB_Excluir_PesoActionPerformed

