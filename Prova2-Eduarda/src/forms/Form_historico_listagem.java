    public void preencherTabela() {
        HistoricoDAO histdao = new HistoricoDAO();
        List<Historico> lista = histdao.listarHistoricos();
        DefaultTableModel tabela = (DefaultTableModel) jT_Pesos.getModel();
        tabela.setRowCount(0);
        for (Historico h : lista) {
            tabela.addRow(new Object[]{h.getAluno().getIdal(), h.getAluno().getNome(), h.getDataHist(), h.getAluno().getAltura(), h.getAluno().getPeso(), h.getPeso(), h.getIdhist()});
        }
    }

    public void preencherTabela(String nomepesquisa) {
        HistoricoDAO histdao = new HistoricoDAO();
        List<Historico> lista = histdao.buscaNome(nomepesquisa);

        DefaultTableModel tabela = (DefaultTableModel) jT_Pesos.getModel();
        tabela.setRowCount(0);
        for (Historico h : lista) {
            tabela.addRow(new Object[]{h.getAluno().getIdal(), h.getAluno().getNome(), h.getDataHist(), h.getAluno().getAltura(), h.getAluno().getPeso(), h.getPeso(), h.getIdhist()});
        }
    }

